/**
 * FOR LOOP
 *    - bentuk umum : 
 *      for(nilai awal, kondisi terminasi/pengulangan, increment/decrement) {
 *        aksi;
 *      }
 * 
 *    - contoh :                                                   _________________________________________
 *      for(i=1; i<=3; i++) {            output     hello 1       | i    : nilai awal.                      |
 *        console.log('hello ' + i);  ----------->  hello 2       | i<=3 : kondisi perulangan.              |
 *      }                                           hello 3       | i++  : increment (penambahan nilai) /   |
 *                                                                |        decrement (pengurangan nilai)    |
 *                                                                |_________________________________________|
 */